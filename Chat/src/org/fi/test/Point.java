package org.fi.test;

public class Point {
	   private int xPoint;
	    private double yPoint;

	    public Point(int xPoint, double yPoint) {
	        this.xPoint = xPoint;
	        this.yPoint = yPoint;
	    }

	    public int getXPoint() {
	        return xPoint;
	    }

	    public double getYPoint() {
	        return yPoint;
	    }
}
